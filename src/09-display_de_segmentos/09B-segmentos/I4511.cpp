void I4511::SetSegmentos( uint16_t valor )
{
	if ( ! ( valor & 1 ) )
		m_bcd[ 0 ]->ClrPin();
	else
		m_bcd[ 0 ]->SetPin();

	if ( ! (( valor >> 1 )  & 1 ) )
		m_bcd[ 1 ]->ClrPin();
	else
		m_bcd[ 1 ]->SetPin();

	if ( ! (( valor >> 2 )  & 1 ) )
		m_bcd[ 2 ]->ClrPin();
	else
		m_bcd[ 2 ]->SetPin();

	if ( ! (( valor >> 3 )  & 1 ) )
		m_bcd[ 3 ]->ClrPin();
	else
		m_bcd[ 3 ]->SetPin();

	m_bcd[ 4 ]->SetPin();

}
