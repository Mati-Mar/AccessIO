void I4017::SetReset( void )
{
	m_pins4017[ 0 ]->ClrPin( );
	m_pins4017[ 0 ]->SetPin( );	m_pins4017[ 0 ]->ClrPin( );

	m_indice = 0;
}

void I4017::SetClock( void )
{
	m_pins4017[ 0 ]->ClrPin( );

	m_pins4017[ 1 ]->SetPin( );
	m_pins4017[ 1 ]->SetPin( );
	m_pins4017[ 1 ]->ClrPin( );
}

void I4017::SetDigito( void )
{
	if( m_indice < m_maxsalidas )
		SetClock( );
	else
		SetReset();

	m_indice++;
}

