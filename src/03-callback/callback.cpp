Callback::Callback( void (*CallBack) (void) ):m_CallBack(CallBack)
{
	if ( m_CallBack )
		g_CallbackList.push_back( this );
}

Callback::~Callback() {
}

void Callback::callback(void)
{
	if ( m_CallBack )
		m_CallBack();
}
